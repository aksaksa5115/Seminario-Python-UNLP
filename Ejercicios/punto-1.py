# Escribe un programa que solicite al usuario su año de nacimiento y muestre en
# qué año cumplirá 18, 21 y 100 años.

año_del_usuario = int(input("Ingrese su año de nacimiento: "))
print("Cumpliras 18 años en el año :", año_del_usuario + 18,
"cumpliras 21 en el año :", año_del_usuario + 21,
"cumpliras 100 en el año :", año_del_usuario + 100)
